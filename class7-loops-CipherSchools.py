# While Loop 
# The while statement allows you to repeatedly execute a block of statements as long as a condition is true.
a=1
while a < 10:
    print(a)
    a+=1

# FOr loop 
# the for..in statement is another looping statement which iterates over a sequence of objects o.e. go through each other items in a sequence
for i in "hello":
    print(i)